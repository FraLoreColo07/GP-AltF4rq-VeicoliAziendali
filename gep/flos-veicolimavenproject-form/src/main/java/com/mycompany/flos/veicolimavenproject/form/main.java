    public void generaDati(String id) {

        MyQuery query = new MyQuery();
        listUtilizzi = new ArrayList<Utilizzo>();
        labelUtente.setText(id);
        System.out.println(id);
        String dati = query.getData(id, "altf4_utilizzo");
        String[] list = dati.split(";");

        if (list.length > 0 && !"".equals(list[0])) {

            for (int i = 0; i < list.length; i++) {

                String[] values = list[i].split(",");
                String data = Date.valueOf(values[1]).toString();
                String oraIn = Time.valueOf(values[2]).toString();
                String oraOut = Time.valueOf(values[3]).toString();
                listUtilizzi.add(new Utilizzo(values[0], data, oraIn, oraOut, values[4], values[6],values[7]));
            }
        }

        if (listUtilizzi.size() > 0) {

            System.out.println("si");
            String header[] = new String[]{"ID", "Destinazione", "oraIn", "oraOut"};
            tbUtilizzo.setColumnIdentifiers(header);
            tableUtilizzi.setModel(tbUtilizzo);
            for (int i = 0; i < listUtilizzi.size(); i++) {

                tbUtilizzo.addRow(new Object[]{listUtilizzi.get(i).getID(), listUtilizzi.get(i).getDestinazione(), listUtilizzi.get(i).getOraIn(), listUtilizzi.get(i).getOraOut()});

            }

        }
        
        
        String data1 = query.getData(id, "altf4_prenotazione");
        String[] line1 = data1.split(";");
        
         if (line1.length > 0 ) {

            for (int i = 0; i < line1.length; i++) {

                String[] values1 = list[i].split(",");
                String dataO = Date.valueOf(values1[3]).toString();
                String oraIn1 = Time.valueOf(values1[4]).toString();
                listPre.add(new Prenotazione(values1[0], values1[1], values1[2], dataO, oraIn1,values1[5]));
                
            }
        }
         
         if (listPre.size() > 0) {

            System.out.println("si");
            String header[] = new String[]{"ID", "Destinazione", "data", "oraIn"};
            tbPrenota.setColumnIdentifiers(header);
            tbPre.setModel(tbPrenota);
            for (int i = 0; i < listUtilizzi.size(); i++) {

                tbPrenota.addRow(new Object[]{listPre.get(i).getID(), listPre.get(i).getDestinazione(), listPre.get(i).getData(), listPre.get(i).getOraIn()});

            }

        }
    }

